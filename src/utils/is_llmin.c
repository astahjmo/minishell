#include "minishell.h"

int	is_llmin(char *str)
{
	char	*llmin;

	llmin = "-9223372036854775808";
	while (*str)
	{
		if (*str != *llmin)
			return (0);
		str++;
		llmin++;
	}
	return (1);
}
